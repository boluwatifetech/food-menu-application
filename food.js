let container = document.getElementById('full-container');
const filterBtn = document.querySelectorAll('.filter-btn')
filterBtn.forEach(function(btn) {
    btn.addEventListener('click', function(e) {
        const category = e.currentTarget.dataset.class;
        const menuCategory = menu.filter(function(menuItems) {
            if (menuItems.category === category) {
                return menuItems;
            }
        })
        if (category === 'all') {
            displayFoodItem(menu);
        } else {
            displayFoodItem(menuCategory);
        }
    })
})


window.addEventListener('DOMContentLoaded', function() {
    displayFoodItem(menu);
});

function displayFoodItem(itemsMenu) {
    let displayMenu = itemsMenu.map(function(item) {

        return `<article class="article">
    <div class="img-container">
        <img src=${item.image} class='photo'>
    </div>
    <div class="known">
        <span>
        <h4 id="food">${item.dish}</h4>
        <h5 id="price">$${item.amount}</h5>
       </span>
        <hr>
        <p id="text">  ${item.word}</p>
    </div>
</article>`;
    });
    displayMenu = displayMenu.join('')
    container.innerHTML = displayMenu;
}